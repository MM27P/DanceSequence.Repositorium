export class Count {
    public danceNumber: number = 0;
    public moveNumber: number = 0;
    public sequenceNumber: number = 0;

    constructor(danceNumber: number, moveNumber: number, sequenceNumber: number) {
       this.danceNumber = danceNumber;
       this.moveNumber = moveNumber;
       this.sequenceNumber = sequenceNumber;
    }
}